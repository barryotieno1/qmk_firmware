bool rgb_matrix_indicators_user(void) {
    switch (get_highest_layer(layer_state)) {
        case _COLEMAK:
            rgb_matrix_set_color_all(0,0,0);
            break;
        case _QWERTY:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color(29, 255, 128, 0);
            rgb_matrix_set_color(30, 255, 128, 0);
            rgb_matrix_sethsv(0,0,64);
            break;
        case _L1:
            rgb_matrix_set_color_all(0,0,0);
            break;
        case _L2:
            rgb_matrix_set_color_all(0,0,0);
            break;
         case _WKS:
            rgb_matrix_set_color_all(0,0,0);
            break;
        default:
            break;
    }
    return true;
}

